//!
//! - [`StatusCode::PermissionDenied`] - Policy or authorization violations
//! - [`StatusCode::FailedPrecondition`] - General business rule violations
//! - [`StatusCode::ResourceExhausted`] - Rate limiting, quota exceeded
//! - [`StatusCode::InvalidArgument`] - Validation failures
//! - [`StatusCode::Aborted`] - Conflict/concurrency issues
//!
