//! Build script for lore-server that:
//! 1. Auto-generates `mod.rs` files for `plugins/` and `hooks/` directories
//! 2. Creates `register_all_plugins()` and `register_all_hooks()` functions
//! 3. Populates environment with build details via vergen

use std::collections::BTreeSet;
use std::error::Error;
use std::fmt::Write as FmtWrite;
use std::fs;
use std::io::Write;
use std::path::Path;

include!("../build-helper.rs");

/// Files to exclude from code generation (infrastructure files).
///
/// These files are part of the plugin/hook infrastructure and are always
/// explicitly declared in the generated mod.rs. They are not auto-discovered.
const EXCLUDED_FILES: &[&str] = &[
    "mod.rs",
    "registry.rs",
    "traits.rs",
    "dispatch.rs",
    "context.rs",
];

/// Modules that require feature flags to compile.
/// Maps module name to required feature flag.
/// Currently empty - all modules are included unconditionally based on their
/// source file existence in the plugins/hooks directories.
const FEATURE_GATED_MODULES: &[(&str, &str)] = &[];

/// Header comment for generated files.
const GENERATED_FILE_HEADER: &str = r#"// ============================================================================
// AUTO-GENERATED FILE - DO NOT EDIT MANUALLY
// ============================================================================
// This file is automatically generated by build.rs during compilation.
// Any manual changes will be overwritten on the next build.
//
// To add a new plugin/hook:
// 1. Create a new .rs file in the appropriate directory (plugins/ or hooks/)
// 2. Implement the required traits and register() function
// 3. Run `cargo build` - this file will be regenerated automatically
// ============================================================================

"#;

fn main() -> Result<(), Box<dyn Error>> {
    // Generate mod.rs files for plugins and hooks
    generate_plugins_mod()?;
    generate_hooks_mod()?;

    // Set up rerun-if-changed directives for the directories
    println!("cargo:rerun-if-changed=src/plugins");
    println!("cargo:rerun-if-changed=src/hooks");

    // Compile legacy urc.rpc protos (storage + revision) that the server still serves for
    // backward compatibility but that no client crate links against
    compile_legacy_protos()?;

    // Populate environment with build details
    vergen::Emitter::default()
        .add_instructions(&vergen::BuildBuilder::all_build()?)?
        .add_instructions(&vergen::RustcBuilder::all_rustc()?)?
        .add_instructions(&vergen::SysinfoBuilder::all_sysinfo()?)?
        .add_custom_instructions(&LoreVergen::default())?
        .emit()?;

    Ok(())
}

/// Compile the legacy urc.rpc.{Storage,Revision,Repository,Environment}Service protos that now live in this crate. `urc.model` messages stay in `lore-proto`; `extern_path` redirects generated references there so the types are not re-declared.
fn compile_legacy_protos() -> Result<(), Box<dyn Error>> {
    let crate_dir = std::env::var("CARGO_MANIFEST_DIR")?;
    let output_dir = std::path::PathBuf::from(&crate_dir)
        .join("src")
        .join("legacy")
        .join("generated");
    fs::create_dir_all(&output_dir)?;

    println!("cargo:rerun-if-changed=src/legacy/proto/storage.proto");
    println!("cargo:rerun-if-changed=src/legacy/proto/revision.proto");
    println!("cargo:rerun-if-changed=src/legacy/proto/repository.proto");
    println!("cargo:rerun-if-changed=src/legacy/proto/environment.proto");
    println!("cargo:rerun-if-changed=../lore-proto/proto/model.proto");

    let mut config = tonic_prost_build::Config::new();
    config.enable_type_names();
    config.bytes(["."]);
    config.extern_path(".urc.model", "::lore_proto::model");

    tonic_prost_build::configure()
        .out_dir(&output_dir)
        .protoc_arg("--experimental_allow_proto3_optional")
        .compile_with_config(
            config,
            &[
                "./src/legacy/proto/storage.proto",
                "./src/legacy/proto/revision.proto",
                "./src/legacy/proto/repository.proto",
                "./src/legacy/proto/environment.proto",
            ],
            &["./src/legacy/proto", "../lore-proto/proto"],
        )?;

    Ok(())
}

/// Represents a discovered module with optional feature gate.
#[derive(Debug, Clone, PartialEq, Eq, PartialOrd, Ord)]
struct DiscoveredModule {
    name: String,
    feature: Option<String>,
}

impl DiscoveredModule {
    fn new(name: String) -> Self {
        // Check if this module requires a feature flag
        let feature = FEATURE_GATED_MODULES
            .iter()
            .find(|(module, _)| *module == name)
            .map(|(_, feature)| (*feature).to_string());

        Self { name, feature }
    }
}

/// Discovers module names from `.rs` files in a directory.
///
/// Returns a sorted set of modules (without `.rs` extension),
/// excluding infrastructure files defined in `EXCLUDED_FILES`.
/// Feature-gated modules are tagged with their required feature.
fn discover_modules(dir: &Path) -> Result<BTreeSet<DiscoveredModule>, Box<dyn Error>> {
    let mut modules = BTreeSet::new();

    if !dir.exists() {
        return Ok(modules);
    }

    for entry in fs::read_dir(dir)? {
        let entry = entry?;
        let path = entry.path();

        // Only process .rs files
        if path.extension().is_some_and(|ext| ext == "rs")
            && let Some(file_name) = path.file_name().and_then(|n| n.to_str())
        {
            // Skip excluded infrastructure files
            if EXCLUDED_FILES.contains(&file_name) {
                continue;
            }

            // Extract module name (filename without .rs)
            let module_name = file_name.trim_end_matches(".rs");
            modules.insert(DiscoveredModule::new(module_name.to_string()));
        }
    }

    Ok(modules)
}

/// Computes a simple hash of content for idempotency checks.
///
/// Uses a simple FNV-1a hash which is sufficient for detecting content changes.
fn compute_content_hash(content: &str) -> u64 {
    const FNV_OFFSET_BASIS: u64 = 0xcbf29ce484222325;
    const FNV_PRIME: u64 = 0x100000001b3;

    let mut hash = FNV_OFFSET_BASIS;
    for byte in content.bytes() {
        hash ^= byte as u64;
        hash = hash.wrapping_mul(FNV_PRIME);
    }
    hash
}

/// Writes content to a file only if it differs from the existing content.
///
/// This ensures idempotent builds - if the content hasn't changed,
/// the file timestamp remains unchanged and cargo won't trigger unnecessary rebuilds.
fn write_if_changed(path: &Path, content: &str) -> Result<bool, Box<dyn Error>> {
    // Check if file exists and has the same content
    if path.exists() {
        let existing_content = fs::read_to_string(path)?;
        if compute_content_hash(&existing_content) == compute_content_hash(content) {
            // Content is the same, no need to write
            return Ok(false);
        }
    }

    // Write the new content
    let mut file = fs::File::create(path)?;
    file.write_all(content.as_bytes())?;
    Ok(true)
}

/// Generates the mod.rs file for the plugins directory.
fn generate_plugins_mod() -> Result<(), Box<dyn Error>> {
    let plugins_dir = Path::new("src/plugins");
    let mod_file = plugins_dir.join("mod.rs");

    let modules = discover_modules(plugins_dir)?;

    let mut content = String::new();
    content.push_str(GENERATED_FILE_HEADER);

    // Module documentation
    content.push_str(PLUGINS_MOD_DOC);

    // Infrastructure module declarations (always present)
    writeln!(content, "pub mod registry;")?;
    writeln!(content, "pub mod traits;")?;
    writeln!(content)?;

    // Plugin module declarations (discovered)
    if !modules.is_empty() {
        writeln!(content, "// Auto-discovered plugin modules")?;
        for module in &modules {
            if let Some(feature) = &module.feature {
                writeln!(content, "#[cfg(feature = \"{feature}\")]")?;
            }
            writeln!(content, "pub mod {};", module.name)?;
        }
        writeln!(content)?;
    }

    // Re-exports
    writeln!(
        content,
        "// Re-export commonly used items at the module level"
    )?;
    writeln!(content, "pub use registry::PluginRegistry;")?;
    writeln!(content, "pub use traits::ImmutableStorePluginFactory;")?;
    writeln!(content, "pub use traits::LockStorePluginFactory;")?;
    writeln!(content, "pub use traits::MutableStorePluginFactory;")?;
    writeln!(content, "pub use traits::NotificationPlugin;")?;
    writeln!(content, "pub use traits::NotificationPluginContext;")?;
    writeln!(content, "pub use traits::NotificationPluginFactory;")?;
    writeln!(content, "pub use traits::NotificationReceiver;")?;
    writeln!(content, "pub use traits::PluginError;")?;
    writeln!(content, "pub use traits::TopologyPluginFactory;")?;
    writeln!(content)?;

    // Generate register_all_plugins function
    content.push_str(REGISTER_ALL_PLUGINS_DOC);

    if modules.is_empty() {
        writeln!(
            content,
            "    // No plugins discovered - registry will be empty"
        )?;
        writeln!(
            content,
            "    let _ = registry; // Suppress unused variable warning"
        )?;
    } else {
        for module in &modules {
            if let Some(feature) = &module.feature {
                writeln!(content, "    #[cfg(feature = \"{feature}\")]")?;
            }
            writeln!(content, "    {}::register(registry);", module.name)?;
        }
    }
    writeln!(content, "}}")?;

    // Write the file only if content changed
    let changed = write_if_changed(&mod_file, &content)?;
    if changed {
        println!(
            "cargo:info=Generated plugins/mod.rs with {} plugin(s)",
            modules.len()
        );
    }

    Ok(())
}

